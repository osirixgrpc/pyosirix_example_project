class IntegerMathematics:
    """ A class that perform basic integer operations.
    """
    @staticmethod
    def addition(x: int, y: int) -> int:
        """ A method for adding two integers together.

        Args:
             x (int): The first integer.
             y (int): The second integer.

        Returns:
            int: The sum of the two integers.
        """
        return x + y

    @staticmethod
    def subtraction(x: int, y: int) -> int:
        """ A method for subtracting two integers.

        Args:
            x (int): The first integer.
            y (int): The second integer.

        Returns:
            int: The difference of the two integers.
        """
        return x - y

    @staticmethod
    def multiplication(x: int, y: int) -> int:
        """ A method for multiplying two integers.

        Args:
            x (int): The first integer.
            y (int): The second integer.

        Returns:
            int: The product of the two integers.
        """
        return x * y

    @staticmethod
    def division(x: int, y: int) -> int:
        """ A method for dividing two integers.

        Args:
            x (int): The first integer.
            y (int): The second integer.

        Returns:
            int: The quotient of the two integers.
        """
        return x / y
